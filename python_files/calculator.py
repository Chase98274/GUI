def add(value_1, value_2):
    """this function adds two numbers together"""
    sum = value_1 + value_2
    return sum

a = float(input("Enter value one: "))
b = float(input("Enter value two: "))

print(add(a, b))

def subtraction(value_1, value_2):
    difference = value_1 - value_2
    return difference

print(subtraction(a, b))